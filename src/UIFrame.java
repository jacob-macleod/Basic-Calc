    private void plusBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_plusBtnActionPerformed
        //Append a "+" and a "" to calculation queue. "" will hold room for new numbers to be inputted after the user
        //has pressed the "+" button
        calculationQueue = Arrays.copyOf(calculationQueue, calculationQueue.length+2);
        calculationQueue[calculationQueue.length - 2] = "+";
        calculationQueue[calculationQueue.length - 1] = "";
    }//GEN-LAST:event_plusBtnActionPerformed

