    private void equalsBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_equalsBtnActionPerformed
        outputLbl.setText("Calculating...");
        System.out.println(Arrays.toString(calculationQueue));
        //Calculate
        calculation = valueOf(calculationQueue[0]) + valueOf(calculationQueue[2]);
        for (int i=0;i<calculationQueue.length;i++) {
            if (calculationQueue[i] != "+" &&  i > 2) {
                calculation = calculation + valueOf(calculationQueue[i]);
            } 
        }
        outputLbl.setText(String.valueOf(calculation));
    }//GEN-LAST:event_equalsBtnActionPerformed

